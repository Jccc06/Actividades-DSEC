let numero1 = Number(prompt("Dime un numero (1 - 7): "));
switch(numero1) {
    case 1:
        alert("Lunes");
    break;
    case 2:
        alert("Martes");
    break;
    case 3:
        alert("Miercoles");
    break;
    case 4:
        alert("Jueves");
    break;
    case 5:
        alert("Viernes");
    break;
    case 6:
        alert("Sabado");
    break;
    case 7:
        alert("Domingo");
    break;
};